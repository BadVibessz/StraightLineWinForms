namespace StraightLineLibrary
{
    


public class Plane
{
    public double A { get; set; }
    public double B { get; set; }
    public double C { get; set; }
    public double D { get; set; }

    public Plane(double a, double b, double c, double d)
    {
        A = a;
        B = b;
        C = c;
        D = d;
    }
}
}